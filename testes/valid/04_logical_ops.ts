// Exemplo 4: Operações lógicas e comparações
let x: number = 10;
let y: number = 20;

let maior: boolean = x > y;
let menor: boolean = x < y;
let igual: boolean = x == y;
let diferente: boolean = x != y;

let condicao1: boolean = true;
let condicao2: boolean = false;

let e_logico: boolean = condicao1 && condicao2;
let ou_logico: boolean = condicao1 || condicao2;
let negacao: boolean = !condicao1;

console.log("Maior:", maior);
console.log("E lógico:", e_logico);
console.log("OU lógico:", ou_logico);
console.log("Menor:", menor);
console.log("Igual:", igual);
console.log("Diferente:", diferente);
console.log("Condicao1:", condicao1);
console.log("Condicao2:", condicao2);
console.log("Negacao:", negacao);
console.log("x:", x);
console.log("y:", y);
